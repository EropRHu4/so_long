#include "../so_long.h"

int	exit_hook(t_mlx *mlx)
{
	int		i;
	char	**map;

	map = mlx->game.map;
	i = -1;
	while (map[++i])
		free(map[i]);
	free(map);
	exit(EXIT_SUCCESS);
}
